    public void InputKosongException() throws InputKosongException{
        if(!maleRadioBtn.isSelected() && !femaleRadioBtn.isSelected() || fullNameInput.getText().isBlank() || usernameInput.getText().isBlank() || passwordInput.getText().isBlank() || confirmPasswordInput.getText().isBlank() || emailInput.getText().isBlank() || idNumberInput.getText().isBlank() || phoneNumberInput.getText().isBlank()){
            throw new InputKosongException("Input User Tidak Boleh Kosong!");
        }
    }
    
    public void ConfirmPasswordException() throws ConfirmPasswordException{
        if(!passwordInput.getText().equals(confirmPasswordInput.getText())){
            throw new ConfirmPasswordException("Salah konfirmasi password!");
        }
        
    }
