    private void createAccountBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccountBtnActionPerformed
         try{
            Long.parseLong(phoneNumberInput.getText());
            Long.parseLong(idNumberInput.getText());
            boolean check = memberControl.isAccountExists(usernameInput.getText(), passwordInput.getText());
            if(check == true){
                JOptionPane.showMessageDialog(this, "Username dan Password sudah ada!"); 
            }else{
                
            ConfirmPasswordException();
            InputKosongException();
            TWallet TW = new TWallet(0);
            twalletControl.insertTWallet(TW);
            
            
            if(maleRadioBtn.isSelected()){
                Member M = new Member(fullNameInput.getText(),emailInput.getText(),phoneNumberInput.getText(),"Male",usernameInput.getText(),passwordInput.getText(),TW);
                memberControl.insertMember(M);
                
            }else if(femaleRadioBtn.isSelected()){
                Member M = new Member(fullNameInput.getText(),emailInput.getText(),phoneNumberInput.getText(),"Female",usernameInput.getText(),passwordInput.getText(),TW);
                memberControl.insertMember(M);
            }
            
            }
            
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(this, "Input User Tidak Boleh Kosong!");
        }catch(ConfirmPasswordException e){
            JOptionPane.showMessageDialog(this, "Salah konfirmasi password!");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Nomor KTP dan Phone Number Harus Angka Semua!!");
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_createAccountBtnActionPerformed

